using UnityEngine;
using System.Collections;

public class Rectangle
{
	public double x;
	
	public double y;
	
	public double width;
	
	public double height;
	
	public	Rectangle(double _x, double _y, double _width, double _height)
	{
		x = _x;
		
		y = _y;
		
		width = _width;
		
		height = _height;
	}
}
